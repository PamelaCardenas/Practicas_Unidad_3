    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        Thread tHilo = new Thread(){
            @Override
            public void run() {
                super.run(); //To change body of generated methods, choose Tools | Templates.
                while(true){
                    String sTexto = txtAreaNotas.getText();
                    int iCont = 0;
                    for (int i = 0; i < sTexto.length(); i++) {
                        if(sTexto.charAt(i) == 'A' || sTexto.charAt(i) == 'a'){
                        iCont++;
                    }
                    }
                    jLabel1.setText("Cantidad de A = " + iCont);
                    
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(BlocNotas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            
        };
        tHilo.start();
    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        //ABRIR ARCHIVO DE TEXTO
        InputStream isAbrir = null;
        InputStreamReader isBytes = null;
        BufferedReader brTexto = null;
        
        JFileChooser jfOpen = new JFileChooser();
        
        if(jfOpen.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){ //Cuadro de dialogo para abrir el archivo
            
            try {
                
            isAbrir = Files.newInputStream(jfOpen.getSelectedFile().toPath()); //Abrir el archivo
            isBytes = new InputStreamReader(isAbrir); //Leer byte por byte
            brTexto = new BufferedReader(isBytes); //
            
            //LEER EL TEXTO
            String sCade = brTexto.readLine();
            
            while(sCade != null){
                txtAreaNotas.append(sCade + "\n");
                sCade = brTexto.readLine();
            }
            brTexto.close();
            } catch (IOException ex) {
                Logger.getLogger(BlocNotas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
         //ABRIR ARCHIVO DE TEXTO
        OutputStream osAbrir = null;
        OutputStreamWriter osGuardarBytes = null;
        BufferedWriter brTexto = null;
        
        JFileChooser jfOpen = new JFileChooser();
        
        if(jfOpen.showSaveDialog(this) == JFileChooser.APPROVE_OPTION){ //Cuadro de dialogo para abrir el archivo
            
            try {
                
            osAbrir = Files.newOutputStream(jfOpen.getSelectedFile().toPath()); //Abrir el archivo
            osGuardarBytes = new OutputStreamWriter(osAbrir); //Leer byte por byte
            brTexto = new BufferedWriter(osGuardarBytes); //
            
            brTexto.write(txtAreaNotas.getText());
            brTexto.flush();
            brTexto.close();
            } catch (IOException ex) {
                Logger.getLogger(BlocNotas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

